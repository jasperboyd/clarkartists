	/**
	 * Store a newly created resource in storage.
	 *
	 * @return Response
	 */
	public function store()
	{
		$s = $this->user->create(Input::all());

    	if($s->isSaved())
    	{
      	return Redirect::route('users.index')
        	->with('flash', 'The new user has been created');
   		 }

    	return Redirect::route('users.create')
    	  ->withInput()
    	  ->withErrors($s->errors());
 		 }
	}

